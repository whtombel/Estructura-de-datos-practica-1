B=["nevera", "olla", "lavadora", "gancho", "cama", "televisor"]


# Ordenamos en forma descendente
def organizar (lista):
    lista.sort()
    return lista

print ("la lista de cosas del hogar desorganizada es: " , B)
print("la cosas del hogar organizado en forma ascendente es la siguiente: ", organizar(B))

